package binary;

public class BinaryTest {
	
	//2진수, 8진수, 16진수
	public static void main(String[] args) {

		int num = 10;
		//0B: 2진수, 0: 8진수, 0X: 16진수
		int bNum = 0B1010; //10
		int oNum = 012;
		int hNum = 0XA;
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(hNum);
		
	}

}
